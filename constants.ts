export const getStormPointColor = (knots: number, status: string): string => {
  // If the storm has lost tropical characteristics, color it grey/slate regardless of wind
  // unless it's Subtropical (SS/SD) which we treat as TS/TD for visualization intensity.
  if (['EX', 'LO', 'WV', 'DB'].includes(status)) {
    return INTENSITY_COLORS.EX;
  }

  // Saffir-Simpson Wind Scale
  if (knots >= 137) return INTENSITY_COLORS.CAT5;
  if (knots >= 113) return INTENSITY_COLORS.CAT4;
  if (knots >= 96)  return INTENSITY_COLORS.CAT3;
  if (knots >= 83)  return INTENSITY_COLORS.CAT2;
  if (knots >= 64)  return INTENSITY_COLORS.CAT1;
  if (knots >= 34)  return INTENSITY_COLORS.TS;
  
  return INTENSITY_COLORS.TD;
};

export const getCategoryLabel = (knots: number): string => {
  if (knots >= 137) return 'Category 5';
  if (knots >= 113) return 'Category 4';
  if (knots >= 96)  return 'Category 3';
  if (knots >= 83)  return 'Category 2';
  if (knots >= 64)  return 'Category 1';
  if (knots >= 34)  return 'Tropical Storm';
  if (knots > 0)    return 'Depression';
  return 'N/A';
};

